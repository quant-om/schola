# Python basic data types
# Integer datatype
a = 6
b = 8
print(f"Integer = {a}")
print(f"Type of number is {type(b)}")

#float datatype
a = 3.6
print(f"the number is {a}")
print(f"the type of the value is {type(a)}")


# string datatype
a = "happy"
b = "-8"
print(f"The string values are {a} and {b}")
print(f"the type of values are {type(a)} and {type(b)}")

#boolean datatype
float = True
string = False
print(f"float is {float}")
print(f"string is {string}")
print(f"string type is {type(string)}")

#None datatype
data = None
print(f"the Data {data} is {type(data)}")


